package com.ibm.activity.currencyconversionfactor.exception;

public class ResourceUpdateException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = -457046752168702489L;

	public ResourceUpdateException() {
		super();
		
	}

	public ResourceUpdateException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

	public ResourceUpdateException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public ResourceUpdateException(String arg0) {
		super(arg0);
		
	}

	public ResourceUpdateException(Throwable arg0) {
		super(arg0);
		
	}
}
