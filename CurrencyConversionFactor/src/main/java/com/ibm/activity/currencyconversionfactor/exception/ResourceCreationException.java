package com.ibm.activity.currencyconversionfactor.exception;

public class ResourceCreationException extends Exception {

	private static final long serialVersionUID = 6662652408408310962L;

	public ResourceCreationException() {
		super();
		
	}

	public ResourceCreationException(String arg0, Throwable arg1, boolean arg2, boolean arg3) {
		super(arg0, arg1, arg2, arg3);
		
	}

	public ResourceCreationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public ResourceCreationException(String arg0) {
		super(arg0);
		
	}

	public ResourceCreationException(Throwable arg0) {
		super(arg0);
		
	}
}
